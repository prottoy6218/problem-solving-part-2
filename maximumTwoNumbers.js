// Write a program to find maximum between two numbers using if else.

// Example
// Input
// Input num1: 10
// Input num2: 20
// Output

// Maximum = 20

function inputNumber(num1, num2){
    if(num1 > num2){
        console.log("Maximum = " + num1);
    }
    else
        console.log("Maximum = " + num2);
}

inputNumber(10, 20);